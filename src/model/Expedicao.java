public class Expedicao {

    private int criado;
    private int pendente;
    private int preparacao;
    private int aguardando; 
    private int enviado;
    private int pronto;
    private int recebido;
    private int processado;
    private int concluido;

    public int getCriado() {
        return criado;
    }

    public void setCriado(int criado) {
        this.criado = criado;
    }

    public int getPendente() {
        return pendente;
    }

    public void setPendente(int pendente) {
        this.pendente = pendente;
    }

    public int getPreparacao() {
        return preparacao;
    }

    public void setPreparacao(int preparacao) {
        this.preparacao = preparacao;
    }

    public int getAguardando() {
        return aguardando;
    }

    public void setAguardando(int aguardando) {
        this.aguardando = aguardando;
    }

    public int getEnviado() {
        return enviado;
    }

    public void setEnviado(int enviado) {
        this.enviado = enviado;
    }

    public int getPronto() {
        return pronto;
    }

    public void setPronto(int pronto) {
        this.pronto = pronto;
    }

    public int getRecebido() {
        return recebido;
    }

    public void setRecebido(int recebido) {
        this.recebido = recebido;
    }

    public int getProcessado() {
        return processado;
    }

    public void setProcessado(int processado) {
        this.processado = processado;
    }

    public int getConcluido() {
        return concluido;
    }

    public void setConcluido(int concluido) {
        this.concluido = concluido;
    }

    

}
